#!/usr/bin/env node

import { mkdirSync, readdirSync, statSync, writeFileSync } from 'node:fs';
import { dirname, join, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import ts from 'typescript';

const __dirname = dirname(fileURLToPath(import.meta.url));
const packageRoot = resolve(__dirname, '..');
const srcRoot = join(packageRoot, 'src');
const outDir = join(srcRoot, 'public-api');

const groups = {
  components: 'components',
  config: 'config',
  constants: 'constants',
  hooks: 'hooks',
  lib: 'lib',
  pages: 'pages',
  services: 'services',
  shared: 'shared',
  stores: 'stores',
  utils: 'utils',
};

const excludedPathParts = new Set(['__tests__', '__mocks__', 'public-api']);

function isSourceFile(filePath) {
  return filePath.endsWith('.ts') || filePath.endsWith('.tsx');
}

function shouldSkip(filePath) {
  const parts = relative(srcRoot, filePath).split(/[\\/]/);
  if (parts.some((part) => excludedPathParts.has(part))) return true;
  const name = parts[parts.length - 1] ?? '';
  return (
    name.startsWith('index.') ||
    name.includes('.test.') ||
    name.includes('.backup.') ||
    name.endsWith('.module.css.d.ts')
  );
}

function collectSourceFiles(dir) {
  const result = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const fullPath = join(dir, entry.name);
    if (entry.isDirectory()) {
      result.push(...collectSourceFiles(fullPath));
      continue;
    }
    if (!entry.isFile() || !isSourceFile(fullPath) || shouldSkip(fullPath)) continue;
    result.push(fullPath);
  }
  return result.sort();
}

function toExportPath(fromDir, filePath) {
  const withoutExt = filePath.replace(/\.(tsx|ts)$/, '');
  let specifier = relative(fromDir, withoutExt).replace(/\\/g, '/');
  if (!specifier.startsWith('.')) specifier = `./${specifier}`;
  return specifier;
}

function hasExportModifier(node) {
  return node.modifiers?.some((modifier) => modifier.kind === ts.SyntaxKind.ExportKeyword) ?? false;
}

function hasDefaultModifier(node) {
  return node.modifiers?.some((modifier) => modifier.kind === ts.SyntaxKind.DefaultKeyword) ?? false;
}

function isTypeDeclaration(node) {
  return ts.isInterfaceDeclaration(node) || ts.isTypeAliasDeclaration(node);
}

function collectExportedNames(filePath) {
  const sourceFile = ts.createSourceFile(filePath, ts.sys.readFile(filePath) ?? '', ts.ScriptTarget.Latest, true);
  const exports = [];

  for (const statement of sourceFile.statements) {
    if (
      (ts.isFunctionDeclaration(statement) ||
        ts.isClassDeclaration(statement) ||
        ts.isEnumDeclaration(statement) ||
        isTypeDeclaration(statement)) &&
      hasExportModifier(statement) &&
      statement.name
    ) {
      exports.push({
        name: statement.name.text,
        typeOnly: isTypeDeclaration(statement),
        defaultExport: hasDefaultModifier(statement),
      });
      continue;
    }

    if (ts.isVariableStatement(statement) && hasExportModifier(statement)) {
      for (const declaration of statement.declarationList.declarations) {
        if (ts.isIdentifier(declaration.name)) {
          exports.push({ name: declaration.name.text, typeOnly: false });
        }
      }
      continue;
    }

    if (ts.isExportDeclaration(statement) && statement.exportClause && ts.isNamedExports(statement.exportClause)) {
      for (const element of statement.exportClause.elements) {
        exports.push({
          name: (element.propertyName ?? element.name).text,
          typeOnly: statement.isTypeOnly || element.isTypeOnly,
          defaultExport: element.propertyName?.text === 'default',
        });
      }
    }
  }

  return exports.sort((a, b) => a.name.localeCompare(b.name));
}

function writeBarrel(name, sourceSubdir) {
  const sourceDir = join(srcRoot, sourceSubdir);
  const files = statSync(sourceDir).isDirectory() ? collectSourceFiles(sourceDir) : [];
  const exportedNames = new Set();
  const lines = [
    '/*',
    ' * *',
    ' *  * Copyright (C) Huawei Technologies Co., Ltd. 2026. All rights reserved.',
    ' *',
     ' */',
     '',
     '// Generated by scripts/generate-web-public-api.mjs.',
  ];

  for (const filePath of files) {
    const names = collectExportedNames(filePath).filter((item) => !exportedNames.has(item.name));
    for (const item of names) {
      exportedNames.add(item.name);
    }
    if (names.length === 0) continue;

    const specifier = toExportPath(outDir, filePath);
    const defaultNames = names.filter((item) => item.defaultExport).map((item) => item.name);
    const valueNames = names.filter((item) => !item.typeOnly && !item.defaultExport).map((item) => item.name);
    const typeNames = names.filter((item) => item.typeOnly).map((item) => item.name);
    for (const exportName of defaultNames) {
      lines.push(`export { default as ${exportName} } from '${specifier}';`);
    }
    if (valueNames.length > 0) {
      lines.push(`export { ${valueNames.join(', ')} } from '${specifier}';`);
    }
    if (typeNames.length > 0) {
      lines.push(`export type { ${typeNames.join(', ')} } from '${specifier}';`);
    }
  }

  lines.push('');
  writeFileSync(join(outDir, `${name}.ts`), lines.join('\n'));
}

mkdirSync(outDir, { recursive: true });
for (const [name, subdir] of Object.entries(groups)) {
  writeBarrel(name, subdir);
}
